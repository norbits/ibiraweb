package br.com.ibiraweb.bean;

public class DetalhePago {

	private String idJovem;
	private String dataPagamento;
	private String valorPagamento;
	private String idTipoFormaPgto;
	private String competencia;
	private String isento;
	
	
	
	public String getIdJovem() {
		return idJovem;
	}
	public void setIdJovem(String idJovem) {
		this.idJovem = idJovem;
	}
	public String getDataPagamento() {
		return dataPagamento;
	}
	public void setDataPagamento(String dataPagamento) {
		this.dataPagamento = dataPagamento;
	}
	public String getValorPagamento() {
		return valorPagamento;
	}
	public void setValorPagamento(String valorPagamento) {
		this.valorPagamento = valorPagamento;
	}
	public String getIdTipoFormaPgto() {
		return idTipoFormaPgto;
	}
	public void setIdTipoFormaPgto(String idTipoFormaPgto) {
		this.idTipoFormaPgto = idTipoFormaPgto;
	}
	public String getCompetencia() {
		return competencia;
	}
	public void setCompetencia(String competencia) {
		this.competencia = competencia;
	}
	public String getIsento() {
		return isento;
	}
	public void setIsento(String isento) {
		this.isento = isento;
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
